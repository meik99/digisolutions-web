<div class="row text-justify">
    <div class="col-sm-12 col-md-8 col-lg-8 col-xl-8">
        Alles begann 1983 mit dem Einstieg in die Welt der gerade erschienenen 8-Bit
        Computer wie Commodore C64 bzw. Atari 800 XL. Ich wechselte 1986 von meinem
        erlernten Beruf in die EDV-Branche. Der weitere Weg führte über  CPM-Rechnern wie
        Schneider Joyce, Apple, DOS, Windows 3.2 zu den heutigen Standards wie Windows10, Linux
        und Android für mobile Geräte. In dieser langen Zeit beschäftigte ich mich auch
        intensiver mit DTP-Systemen, POS-Lösungen und fairen Schulungen für OpenSource Produkte
        und Senioren. 2010/11 waren eine Reihe von persönlichen Gründen dafür verantwortlich, dass
        ich wieder in meinen erlernten Beruf zurück ging und seitdem im besten API-Werk der Welt
        als Chemischer Verfahrenstechniker tätig bin.
    </div>
</div>
<div class="row text-justify">
    <div class="col-sm-12 col-md-8 col-lg-8 col-xl-8">
        EDV blieb nur noch ein Hobby bis ich mich heuer 2018 wieder entschloss ein Kleingewerbe
        nebenbei anzumelden. Einerseits waren die teils abenteuerlichen Geschichten die manche
        bei diversen Händler erlebten und andererseits auch meine Freunde, Bekannten und Kollegen
        dafür verantwortlich. Viele Stimmen waren der Meinung ich sollte wieder in diesem Bereich tätig werden.
        Da ich durch meinem Hauptberuf, den ich auch liebe, meine Lebenserhaltungskosten abdecken
        kann, bin ich auch in der glücklichen Lage alle Serviceleistungen zu fairen Preisen anbieten zu können.
        Ich muss ja nicht viel verdienen nur meine Unkosten sollen hauptsächlich gedeckt sein.
        So hoffe ich dass ich euch wieder mit Rat und Tat zur Seite stehen kann und freue
        mich schon auf viele gelöste Probleme.
